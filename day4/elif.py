# Elif = else if statement

nama = input("Masukan nama kamu :")

if nama == "ucup": # kondisi 1
    print(f"kamu {nama} gateng 1") # aksi true 1
elif nama == "otong": # kondisi 2
    print(f"kamu {nama} gateng 2") # aksi true 2
elif nama == "asep": # kondisi 3
    print(f"kamu {nama} gateng 3") # aksi true 3
else:
    print(f"Terima Kasih {nama}") # aksi false

print("Akhir Program") 