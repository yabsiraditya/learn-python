# For Loop (Perulangan)

# dengan List
angka_list = [0,2,4,6,8,10] # ini adalah list
print(angka_list)

for i in angka_list:
    print(f"i sekarang -> {i}")

print("Ini Akhir Program\n")

# ini dengan Range
angka_range = range(5)

for i in angka_range:
    print(f"i sekarang -> {i}")

print("Ini Akhir Program\n")

angka_range = range(1,10)

for i in angka_range:
    print(f"i sekarang -> {i}")

print("Ini Akhir Program\n")

# menggunkan String
data_str = "Asep Saepudin"

for huruf in data_str:
    print(huruf)

print("Ini Akhir Program\n")